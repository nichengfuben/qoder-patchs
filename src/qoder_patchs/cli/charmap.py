# ---------------------------------------------------------------------------
# Default blue palette for gradient banners
# ---------------------------------------------------------------------------

BLUE_GRADIENT_PALETTE: List[Tuple[int, int, int]] = [
    (20, 80, 255),    # deep blue
    (40, 120, 255),   # medium blue
    (60, 160, 255),   # bright blue
    (80, 200, 255),   # light blue
    (100, 230, 255),  # ice blue
]


# ---------------------------------------------------------------------------
# Multi-color lerp
# ---------------------------------------------------------------------------


def _multi_lerp(
    colors: List[Tuple[int, int, int]],
    factor: float,
) -> Tuple[int, int, int]:
    """Interpolate across a list of RGB colours at *factor* (0.0 .. 1.0).

    The factor is clamped to [0, 1].  The colour list is divided into
    equal segments, and linear interpolation is performed within the
    appropriate segment.

    Args:
        colors: List of (R, G, B) tuples (at least one).
        factor: Position along the gradient, 0.0 = first colour, 1.0 = last.

    Returns:
        An (R, G, B) tuple.
    """
    if not colors:
        return (255, 255, 255)
    if len(colors) == 1:
        return colors[0]
    factor = max(0.0, min(1.0, factor))
    seg = len(colors) - 1
    scaled = factor * seg
    idx = min(int(scaled), seg - 1)
    local = scaled - idx
    c1, c2 = colors[idx], colors[idx + 1]
    return (
        int(c1[0] + (c2[0] - c1[0]) * local),
        int(c1[1] + (c2[1] - c1[1]) * local),
        int(c1[2] + (c2[2] - c1[2]) * local),
    )


