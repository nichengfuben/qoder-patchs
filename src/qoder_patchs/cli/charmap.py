# ---------------------------------------------------------------------------
# Windows ANSI escape support
# ---------------------------------------------------------------------------


def _enable_windows_ansi() -> None:
    """Enable Windows console ANSI escape sequence support.

    Calls the Win32 ``SetConsoleMode`` API to enable
    ``ENABLE_VIRTUAL_TERMINAL_PROCESSING`` (0x0004) on the stdout handle.
    Silently does nothing on non-Windows platforms or if the call fails.
    """
    try:
        import ctypes

        kernel32 = ctypes.windll.kernel32  # type: ignore[attr-defined]
        handle = kernel32.GetStdHandle(-11)
        mode = ctypes.c_ulong()
        kernel32.GetConsoleMode(handle, ctypes.byref(mode))
        kernel32.SetConsoleMode(handle, mode.value | 0x0004)
    except Exception:
        pass


# Enable ANSI on import when running on Windows
if sys.platform == "win32":
    _enable_windows_ansi()


# ---------------------------------------------------------------------------
# ANSI strip helper
# ---------------------------------------------------------------------------

_ANSI_RE = re.compile(r"\033\[[0-9;]*[a-zA-Z]")


def _strip_ansi(text: str) -> str:
    """Remove ANSI escape sequences from *text*, returning plain text."""
    return _ANSI_RE.sub("", text)


