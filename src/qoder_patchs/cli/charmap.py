def render_text(text: str) -> list[str]:
    """Render *text* as 6-line-high ASCII art using :data:`CHAR_MAP`.

    Characters not found in the map are silently skipped.  The input is
    converted to uppercase before lookup, but lowercase glyphs are also
    tried as a fallback.

    Args:
        text: The text string to render.

    Returns:
        A list of exactly 6 strings, each representing one row of the
        rendered art.
    """
    lines = ["", "", "", "", "", ""]
    for ch in text:
        # Try exact match first, then uppercase, then lowercase
        glyph = CHAR_MAP.get(ch)
        if glyph is None:
            glyph = CHAR_MAP.get(ch.upper())
        if glyph is None:
            glyph = CHAR_MAP.get(ch.lower())
        if glyph is not None:
            for i in range(6):
                lines[i] += glyph[i]
    return lines


def render_gradient_banner(
    lines: list[str],
    palette: list[tuple[int, int, int]] | None = None,
) -> str:
    """Apply a diagonal gradient colour effect to pre-rendered ASCII art lines.

    Each non-space character is coloured using an RGB ANSI escape sequence.
    The colour is determined by a diagonal factor: characters at the
    top-left are coloured with the first palette entry, and characters at
    the bottom-right with the last.

    Args:
        lines: A list of strings (typically 6 lines from :func:`render_text`).
        palette: An optional list of (R, G, B) tuples for the gradient.
            Defaults to :data:`BLUE_GRADIENT_PALETTE`.

    Returns:
        A single string with embedded ANSI colour codes, ready to print.
    """
    if palette is None:
        palette = list(BLUE_GRADIENT_PALETTE)

    # Check if terminal supports colour
    try:
        color_ok = hasattr(sys.stdout, "isatty") and sys.stdout.isatty()
    except Exception:
        color_ok = False

    if not color_ok:
        return "\n".join(f"  {line}" for line in lines)

    height = len(lines)
    width = max(len(line) for line in lines) if lines else 0
    max_diag = width + height - 2 if (width + height) > 2 else 1

    reset = "\033[0m"
    result_lines: list[str] = []
    for row, line in enumerate(lines):
        chars: list[str] = []
        for col, ch in enumerate(line):
            if ch == " ":
                chars.append(" ")
                continue
            # Diagonal factor: top-left = 0.0, bottom-right = 1.0
            factor = (col + row) / max_diag
            factor = max(0.0, min(1.0, factor))
            r, g, b = _multi_lerp(palette, factor)
            chars.append(f"\033[38;2;{r};{g};{b}m{ch}")
        chars.append(reset)
        result_lines.append("  " + "".join(chars))

    return "\n".join(result_lines)
